x = 10

if x > 10:
    print("x is greater than 10")
elif x < 10:
    print("x is less than 10")
else:
    print("x is equal to 10")


# Loops : Loops in python allow you to execute a block code repeatedly.
# Example 1 - for loop

fruits = ["apples", "bananas", "cherries"]

for fruit in fruits:
    print(fruit)

# Example 2 - while loop

i = 0

while i < 5:
    print(i)

    i += 1
